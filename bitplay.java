public class bitplay{
    public static void main(String args[]){
        System.out.println(5&6); //bitwise AND output-4
        System.out.println(5|6); //bitwise OR output- 7
        System.out.println(5^6); //bitwise XOR output-3
        System.out.println(~6); // binary one's complement output- -7
        System.out.println(5<<2);//left shift 20
        System.out.println(5>>1);//right shift  2
    }
}